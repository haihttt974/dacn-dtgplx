;WITH HangList AS (
    SELECT idHang, maHang, tenDayDu
    FROM hang
),
Tally AS (
    SELECT TOP (10) ROW_NUMBER() OVER (ORDER BY (SELECT NULL)) AS n
    FROM sys.objects
)
INSERT INTO khoaHoc (tenKhoaHoc, ngayBatDau, ngayKetThuc, slToiDa, moTa, isActive, idHang)
SELECT 
    CONCAT(N'Khóa học lái xe ', H.tenDayDu, N' – K0', T.n) AS tenKhoaHoc,
    DATEADD(DAY, (T.n - 1) * 14, CAST(GETDATE() AS DATE)) AS ngayBatDau,
    DATEADD(DAY, (T.n - 1) * 14 + 45, CAST(GETDATE() AS DATE)) AS ngayKetThuc,
    CASE 
        WHEN H.maHang IN ('A1','A') THEN 60
        WHEN H.maHang IN ('B1','B') THEN 40
        ELSE 30
    END AS slToiDa,
    CONCAT(N'Khóa đào tạo lái xe hạng ', H.maHang, N', khóa số ', T.n, N'.') AS moTa,
    1 AS isActive,
    H.idHang
FROM HangList H
CROSS JOIN Tally T;

GO

