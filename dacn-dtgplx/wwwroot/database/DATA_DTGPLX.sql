DECLARE @khoaHocId INT, @idHang INT, @ngayBatDau DATE;

DECLARE cur CURSOR FOR 
    SELECT k.khoaHocId, k.idHang, k.ngayBatDau
    FROM khoaHoc k;

OPEN cur;
FETCH NEXT FROM cur INTO @khoaHocId, @idHang, @ngayBatDau;

WHILE @@FETCH_STATUS = 0
BEGIN
    DECLARE @soLT INT, @soMP INT, @soTH INT;

    -- Số buổi theo hạng
    IF @idHang IN (SELECT idHang FROM hang WHERE maHang IN (N'A1', N'A'))
        SELECT @soLT = 6, @soMP = 2, @soTH = 6;
    ELSE IF @idHang IN (SELECT idHang FROM hang WHERE maHang IN (N'B1', N'B'))
        SELECT @soLT = 8, @soMP = 4, @soTH = 12;
    ELSE IF @idHang IN (SELECT idHang FROM hang WHERE maHang IN (N'C1', N'C'))
        SELECT @soLT = 10, @soMP = 5, @soTH = 16;
    ELSE
        SELECT @soLT = 12, @soMP = 6, @soTH = 18;

    ----------------------------------------------------------------
    -- LẦY LỚP LÝ THUYẾT NGẪU NHIÊN
    ----------------------------------------------------------------
    DECLARE @lopLyThuyet INT;
    SELECT TOP 1 @lopLyThuyet = lopHocId
    FROM lopHoc
    WHERE tenLop LIKE N'%Lý Thuyết%'
    ORDER BY NEWID();

    -- Tạo buổi Lý thuyết (T2-4-6, 18:00–20:00)
    DECLARE @i INT = 1, @day DATE = @ngayBatDau;
    WHILE @i <= @soLT
    BEGIN
        IF DATENAME(WEEKDAY, @day) IN ('Monday', 'Wednesday', 'Friday')
        BEGIN
            INSERT INTO lichHoc(lopHocId, xeTapLaiId, khoaHocId, ngayHoc, tgBatDau, tgKetThuc, noiDung, diaDiem, ghiChu)
            VALUES(@lopLyThuyet, NULL, @khoaHocId, @day, '18:00', '20:00', N'Học lý thuyết GPLX', N'Phòng A', NULL);
            SET @i += 1;
        END
        SET @day = DATEADD(DAY, 1, @day);
    END

    ----------------------------------------------------------------
